#include "libft.h"

void	ft_putstr_fd(char *s, int fd)
{
	if (fd >= 0)
	{
		if (!s)
			return ;
		while (*s != '\0')
		{
			ft_putchar_fd(*s, fd);
			s++;
		}
	}
}
